#include <iostream>
#include <cstdio>
using namespace std;

int main() {
  int n1;
  long n2;
  char ch;
  float n3;
  double n4;
  double n4;
  scanf("%d %ld %c %f %lf", &n1, &n2, &ch, &n3, &n4);
  printf("%d \n%ld \n%c \n%f \n%lf", n1, n2, ch, n3, n4);
  return 0;
}